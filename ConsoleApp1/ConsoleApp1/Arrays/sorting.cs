// Array sorting

//1. Sorting and integer array
int[] numbers = {45,12,3,98,34};
Array.Sort(numbers);
Console.WriteLine("Sorted integer array: " + string.Join(",",(numbers)));

//2. Sorting an string array
string[] names = {"Kunal","John","Doe","Charlie"};
Array.Sort(names);
Console.WriteLine("Sorted integer array: " + string.Join(",",(names)));