// Operators in C#  
int age = 23;

age++; //It increments the age by 1
System.Console.WriteLine(age);

age--; //It decrements the age by 1
System.Console.WriteLine(age);

age += 10; //It increments the value of the age by 10
System.Console.WriteLine(age);

string name = "Kunal";
name += " is Programming";
System.Console.WriteLine(name);

char ch = 'a';
ch += 'b';
System.Console.WriteLine(ch);

int i = 0;
i++;
System.Console.WriteLine(i);